function dxdt = MPBSR_Dynamics(x, s_in, ~, p, u, c)
% MPBSR_Dynamics: 双稳态系统动力学方程
% 该函数实现了随机共振中经典的双稳态系统的动力学行为，用于描述系统状态随时间的变化规律
% 双稳态系统具有两个稳定状态，在一定噪声作用下可以在两个稳定状态之间跃迁
%
% 输入参数:
%   x: 当前状态，表示系统当前所处的状态值
%   s_in: 当前输入信号值，外部施加的周期性或随机信号
%   ~: 未使用的参数（时间步索引），在函数体中未使用
%   p: 系统参数，控制势垒高度
%   u: 系统参数，控制势间距
%   c: 系统参数，控制开口大小
%
% 输出参数:
%   dxdt: 状态变化率，表示系统状态相对于时间的变化速率 dx/dt

x0 = sqrt(u);  % 计算系统临界状态值，作为分段判断的阈值

% 根据当前状态x与临界值x0的关系，采用不同的动力学方程计算状态变化率
if x < -x0
    % 当前状态小于临界值时的动力学方程
    dxdt = p / 4 / (c - x0) + s_in;
elseif x <= x0
    % 当前状态在特定区间内的标准双稳态系统动力学方程
    dxdt = p / u * x - p / u^2 * x^3 + s_in;
else
    % 当前状态大于临界值时的动力学方程
    dxdt = -p / 4 / (c - x0) + s_in;
end

end